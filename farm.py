
def countAnimalLegs(chickens, cows, pigs):

    return chickens*2 + cows*4 + pigs *4


def main():


    print(f"The total number of animal Legs is: {countAnimalLegs(2,3,5)}")
    print(f"The total number of animal Legs is: {countAnimalLegs(1,2,3)}")
    print(f"The total number of animal Legs is: {countAnimalLegs(5,2,8)}")
    

main()